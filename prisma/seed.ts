async function main() {
  console.log("Seeding database with category groups...");

  let totalCategorias = 0;

  for (const grupo of gruposData) {
    // Criar ou atualizar o grupo
    const grupoCategoria = await prisma.grupoCategoria.upsert({
      where: { slug: grupo.slug },
      update: {
        nome: grupo.nome,
        ordem: grupo.ordem,
      },
      create: {
        nome: grupo.nome,
        slug: grupo.slug,
        ordem: grupo.ordem,
      },
    });

    console.log(`Created/updated group: ${grupo.nome}`);

    // Criar ou atualizar as categorias do grupo
    for (let i = 0; i < grupo.categorias.length; i++) {
      const categoriaNome = grupo.categorias[i];
      const categoriaSlug = slugify(categoriaNome);

      await prisma.categoria.upsert({
        where: { slug: categoriaSlug },
        update: {
          nome: categoriaNome,
          ordem: i + 1,
          grupoId: grupoCategoria.id,
        },
        create: {
          nome: categoriaNome,
          slug: categoriaSlug,
          ordem: i + 1,
          grupoId: grupoCategoria.id,
        },
      });

      totalCategorias++;
    }
  }

  console.log(`Created/updated ${gruposData.length} groups`);
  console.log(`Created/updated ${totalCategorias} categories`);
  console.log("Seeding completed!");
}

main()
  .then(async () => {
    await prisma.$disconnect();
  })
  .catch(async (e) => {
    console.error(e);
    await prisma.$disconnect();
    process.exit(1);
  });
